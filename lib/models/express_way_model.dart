      case 6:
        return AssetImage('assets/images/home/express_way_bangna.jpg');
    }
    return null;
  }
}

class LegModel {
  LegModel({
    @required this.origin,
    @required this.destination,
    @required this.chunkIdList,
    @required this.pointIdList,
    @required this.trafficPointList,
  });

  final String origin;
  final String destination;
  final List<int> chunkIdList;
  final List<int> pointIdList;
  List<TrafficPointModel> trafficPointList;

  factory LegModel.fromJson(Map<String, dynamic> json, List<MarkerModel> markerList) {
    List<MarkerModel> cctvList =
        markerList.where((marker) => marker.category.code == CategoryType.CCTV).toList();
    List<int> pointIdList = json['pointIdList'];

    List<TrafficPointModel> trafficPointList = pointIdList.map<TrafficPointModel>((pointId) {
      TrafficPointModel trafficPoint;

      for (MarkerModel cctv in cctvList) {
        List<CoreConfigModel> filteredCoreConfig = cctv.coreConfigList
            .where((coreConfig) =>
                (coreConfig.code == "markers_alg_point" && int.parse(coreConfig.value) == pointId))
            .toList();

        if (filteredCoreConfig.length > 0) {
          trafficPoint = TrafficPointModel(pointId: pointId, cctvMarkerModel: cctv);
          break;
        }
      }

      return trafficPoint;
    }).toList();

    // filter null ทิ้ง
    trafficPointList = trafficPointList.where((trafficPoint) => trafficPoint != null).toList();

    /*print("++++++++++++++++++++ LEG MODEL ++++++++++++++++++++");
    print("origin: ${json['origin']}");
    print("destination: ${json['destination']}");
    print("trafficPointList: $trafficPointList");
    print("-------------------- LEG MODEL --------------------");*/

    return LegModel(
      origin: json['origin'],
      destination: json['destination'],
      chunkIdList: json['chunkIdList'],
      pointIdList: pointIdList,
      trafficPointList: trafficPointList,
    );
  }
}

class TrafficPointModel {
  // alg point + cctv
  TrafficPointModel({
    @required this.pointId,
    @required this.cctvMarkerModel,
  });

  final int pointId;
  final MarkerModel cctvMarkerModel;

  @override
  String toString() {
    return "{ pointId: ${this.pointId}, cctvMarkerModel: ${this.cctvMarkerModel.toString()} }";
  }
}

class TrafficPointDataModel {
  static const String STATUS_GREEN = "green";
  static const String STATUS_ORANGE = "orange";
  static const String STATUS_RED = "red";
  static const String STATUS_DARK_RED = "dark_red";
  static const String STATUS_UNKNOWN = "unknown";

  TrafficPointDataModel({
    @required this.pointId,
    @required this.status,
    @required this.trafficIndex,
  });

  final int pointId;
  final String status; // สีเส้น: green, orange, red, dark_red
  final double trafficIndex;

  factory TrafficPointDataModel.fromJson(Map<String, dynamic> json) {
    return TrafficPointDataModel(
      pointId: json['point_id'],
      status: json['status'],
      trafficIndex: json['traffic_index'].toDouble(),
    );
  }

  @override
  String toString() {
    return "{ pointId: ${this.pointId}, status: ${this.status}, trafficIndex: ${this.trafficIndex} }";
  }
}

