  factory ExpressWayModel.fromJson(
      Map<String, dynamic> json, int index, List<MarkerModel> markerList) {
    return ExpressWayModel(
      id: json['id'],
      name: json['name'],
      index: index,
      imageUrl: json['cover'],
      chunkDataMap: _chunkDataList[index],
      legList: _chunkDataList[index]['legs']
          .map<LegModel>((legJson) => LegModel.fromJson(legJson, markerList))
          .toList(),
      image: _getMockedAssetImage(index), // mocked
